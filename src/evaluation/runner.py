"""
Evaluation Runner - Uses LangSmith for tracing + evaluation
"""

import asyncio
import json
from pathlib import Path
from datetime import datetime
from typing import List, Dict, Any, Optional
from loguru import logger

from src.observability.langsmith_config import setup_langsmith, get_langsmith_client
from src.evaluation.note_quality import evaluate_note_quality, NoteQualityScore
from src.storage.artifact_store import artifact_store
from src.models.schemas import KnowledgeNote
from src.tools.research_index import research_index


async def evaluate_all_existing_notes(limit: int = 50) -> Dict[str, Any]:
    """
    Retrospective evaluation of all KnowledgeNotes currently in the Artifact Store.
    Results are saved locally and also sent to LangSmith if configured.
    """
    setup_langsmith(project_name="research-agent-evaluation")

    papers_dir = artifact_store.base_dir
    if not papers_dir.exists():
        logger.warning("No papers directory found")
        return {"evaluated": 0}

    results = []
    note_files = list(papers_dir.glob("*/knowledge_note.json"))[:limit]

    logger.info(f"Evaluating {len(note_files)} existing KnowledgeNotes...")

    for note_path in note_files:
        try:
            data = json.loads(note_path.read_text(encoding="utf-8"))
            note = KnowledgeNote(**data)
            score: NoteQualityScore = await evaluate_note_quality(note)

            results.append({
                "paper_id": note.paper_id,
                "title": note.title,
                "completeness": score.completeness,
                "contribution_density": score.contribution_density,
                "specificity": score.specificity,
                "usefulness": score.usefulness,
                "overall": score.overall,
                "feedback": score.feedback[:300] if score.feedback else "",
            })
            logger.info(f"  {note.paper_id}: overall={score.overall:.3f}")
        except Exception as e:
            logger.warning(f"Failed to evaluate {note_path}: {e}")

    # Save results
    run_dir = Path("evaluation_runs") / datetime.now().strftime("%Y%m%d_%H%M%S")
    run_dir.mkdir(parents=True, exist_ok=True)

    (run_dir / "note_scores.json").write_text(
        json.dumps(results, indent=2), encoding="utf-8"
    )

    # Summary
    if results:
        avg_overall = sum(r["overall"] for r in results) / len(results)
        avg_usefulness = sum(r["usefulness"] for r in results) / len(results)
        summary = {
            "evaluated": len(results),
            "average_overall_score": round(avg_overall, 3),
            "average_usefulness": round(avg_usefulness, 3),
            "timestamp": datetime.now().isoformat(),
        }
        (run_dir / "summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")

        # Simple markdown report
        report = f"""# Note Quality Evaluation Report
Generated: {summary['timestamp']}

- Notes evaluated: {summary['evaluated']}
- Average Overall Score: **{summary['average_overall_score']}**
- Average Usefulness (1-5): **{summary['average_usefulness']}**

## Top Notes
"""
        sorted_results = sorted(results, key=lambda x: x["overall"], reverse=True)
        for r in sorted_results[:5]:
            report += f"- `{r['paper_id']}` — {r['overall']:.3f} — {r['title'][:60]}\n"

        report += "\n## Bottom Notes (need improvement)\n"
        for r in sorted_results[-3:]:
            report += f"- `{r['paper_id']}` — {r['overall']:.3f} — {r['title'][:60]}\n"

        (run_dir / "report.md").write_text(report, encoding="utf-8")
        logger.success(f"Evaluation complete. Results saved to {run_dir}")
        return summary

    return {"evaluated": 0}


def create_langsmith_dataset_from_notes(dataset_name: str = "knowledge-notes-v1"):
    """
    Create a LangSmith dataset from existing KnowledgeNotes
    so we can run more sophisticated evaluations later.
    """
    client = get_langsmith_client()
    if not client:
        logger.error("LangSmith client not available")
        return None

    try:
        dataset = client.create_dataset(
            dataset_name=dataset_name,
            description="KnowledgeNotes generated by the Research Agent for quality evaluation"
        )
        logger.success(f"Created LangSmith dataset: {dataset_name}")
        return dataset
    except Exception as e:
        # Dataset may already exist
        logger.warning(f"Could not create dataset (may already exist): {e}")
        return None


if __name__ == "__main__":
    asyncio.run(evaluate_all_existing_notes())

